# mas de una condicion, elegir una ruta de muchas posibles

print("Convertidor de numeros a letras")
numero = int(input("Cual es el numero que deseas convertir? "))
if numero == 1:
    print("UNO")
elif numero == 2:
    print("DOS")
elif numero == 3:
    print("TRES")
elif numero == 4:
    print("CUATRO")
elif numero == 5:
    print("CINCO")
else :
    print("Este programa solo puede convertir del 1 al 5")
print ("fin")
